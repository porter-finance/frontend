export default function TokenLink({
  token,
  withLink = false,
}: {
  token: Token
  withLink?: boolean
}): ReactElement {
  if (!token?.symbol) return <span>-</span>
  const symbol = token.symbol.slice(0, 7)

  if (withLink) {
    return <AddressLink hash={token?.id}>{symbol}</AddressLink>
  }

  return <span>{symbol}</span>
}
